class UniversalModel:

    def __init__(self):
        self.x = None
        self.y = None
        self.a = None
        self.b = None

    def set_x(self, x):
        self.x = x

    def set_y(self, y):
        self.y = y

    def set_a(self, a):
        self.a = a

    def set_b(self, b):
        self.b = b
